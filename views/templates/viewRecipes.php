       <h5 class="card-title">nom de la recette</h5>
       <p class="card-text">
       Faire des crêpes selon la recette classique (ne pas sucrer la pâte à crêpes). Réserver 4 crêpes
 Laver les champignons et les couper en lamelles. Les faire revenir dans un peu d'huile d'olive jusqu'à disparition du jus, puis ajouter la crême fraiche et le jus du citron, saler, poivrer. Laisser mijoter 10 mn à feu doux.
 Quand la préparation est tiède, dresser les ficelles picardes : poser une crêpe à plat sur une assiette, y poser une tranche de jambon, puis au centre sur la longueur le quart de la préparation aux champignons. Rouler le tout et poser dans un plat allant au four.
 Faire la même chose avec les 3 crêpes restantes.
 Saupoudrer les ficelles de gruyère rapé et passer au four à 200° (th 5) pendant 10 minutes. Déguster bien chau
